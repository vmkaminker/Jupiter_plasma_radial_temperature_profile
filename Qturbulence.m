function [Qturb, Lturb] = Qturbulence(num_points)

    Rj_min = 12;
    Rj_max = 35;    

    a = [0.364545219, 0.314847951, 0.299573227, 1.0467846, 0.869338446, 0.721938391, 0.574434891, 0.528462152, 0.469419391, 0.028961793, -0.00375309, -0.069314718, 0.036860464, 0.033891914, -0.003812407, -0.1971867, -0.300924847, -0.448621972, -0.21251866, -0.237092303, -0.326001917, 0.194968789, 0.194968789];
    A = [1.68756E-19, 3.05261E-19, 3.77136E-19, 4.79113E-24, 9.1037E-23, 1.04195E-21, 1.46282E-20, 3.57174E-20, 1.152E-19, 1.08866E-15, 2.28075E-15, 1.13137E-14, 8.25716E-16, 8.57143E-16, 2.28549E-15, 4.51596E-13, 7.50205E-12, 5.93518E-10, 5.09883E-13, 1.08919E-12, 1.80451E-11, 6.52478E-19, 6.52478E-19];
    Qturb = [];
    Lturb = [];

    for i = 1:(Rj_max-Rj_min)
        L = [Rj_min+i-1:1/num_points:Rj_min+i];
        Q = A(i)*exp(a(i)*L);
        Qturb = horzcat (Qturb, Q);
        Lturb = horzcat (Lturb, L);
    end
    
end
